def tcvn3_to_unicode(text):
    return ''.join(chr(_TCVN3_MAP.get(ord(c), ord(c))) for c in text)

def vni_to_unicode(text):
    result, i, n = [], 0, len(text)
    while i < n:
        c1 = ord(text[i])
        if i + 1 < n:
            key = (c1, ord(text[i + 1]))
            if key in VNI_TWO_CHAR:
                result.append(chr(VNI_TWO_CHAR[key]))
                i += 2
                continue
        result.append(chr(VNI_SINGLE.get(c1, c1)))
        i += 1
    return ''.join(result)

