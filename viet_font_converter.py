BG        = "#1A1A24"
BTN_TCVN  = "#4A5CF0"
BTN_VNI   = "#8B4AF0"
BTN_HOV_T = "#6070FF"
BTN_HOV_V = "#A060FF"
TEXT_W    = "#FFFFFF"
TEXT_DIM  = "#8888AA"
SUCCESS   = "#3DD68C"
ERROR_C   = "#FF6B6B"

class MiniConverter(tk.Tk):
    def __init__(self):
        super().__init__()
        self.title("Viet Converter")
        self.resizable(False, False)
        self.attributes("-topmost", True)
        self.configure(bg=BG)
        self._status_job = None
        self._build_ui()
        self.update_idletasks()
        sw, sh = self.winfo_screenwidth(), self.winfo_screenheight()
        w, h = self.winfo_width(), self.winfo_height()
        self.geometry(f"+{sw-w-24}+{sh-h-60}")

    def _build_ui(self):
        # Drag bar
        bar = tk.Frame(self, bg=BG, cursor="fleur")
        bar.pack(fill="x")
        bar.bind("<ButtonPress-1>", lambda e: setattr(self, '_dx', e.x) or setattr(self, '_dy', e.y))
        bar.bind("<B1-Motion>", lambda e: self.geometry(f"+{self.winfo_x()+e.x-self._dx}+{self.winfo_y()+e.y-self._dy}"))
        tk.Label(bar, text="⚡ Viet Converter", font=("Segoe UI", 9, "bold"),
                 fg=TEXT_DIM, bg=BG).pack(side="left", padx=12, pady=7)
        tk.Button(bar, text="✕", font=("Segoe UI", 9), fg=TEXT_DIM, bg=BG,
                  activebackground=ERROR_C, activeforeground=TEXT_W,
                  relief="flat", bd=0, padx=8, cursor="hand2",
                  command=self.destroy).pack(side="right", padx=4)

        # Buttons
        f = tk.Frame(self, bg=BG)
        f.pack(padx=14, pady=(2, 8))
        self.btn_t = self._btn(f, "TCVN3 → Unicode", BTN_TCVN, BTN_HOV_T, self._conv_tcvn3)
        self.btn_t.pack(side="left", padx=(0,6))
        self.btn_v = self._btn(f, "VNI → Unicode",   BTN_VNI,  BTN_HOV_V, self._conv_vni)
        self.btn_v.pack(side="left")

        # Status
        self.sv = tk.StringVar(value="Ctrl+C  →  bấm nút  →  Ctrl+V")
        self.sl = tk.Label(self, textvariable=self.sv, font=("Segoe UI", 8),
                           fg=TEXT_DIM, bg=BG)
        self.sl.pack(pady=(0,8))

        self.bind("<F1>", lambda e: self._conv_tcvn3())
        self.bind("<F2>", lambda e: self._conv_vni())

    def _btn(self, parent, text, color, hover, cmd):
        b = tk.Button(parent, text=text, font=("Segoe UI Semibold", 10),
                      fg=TEXT_W, bg=color, activebackground=hover,
                      activeforeground=TEXT_W, relief="flat", bd=0,
                      padx=18, pady=10, cursor="hand2", command=cmd)
        b._c = color; b._h = hover
        b.bind("<Enter>", lambda e: b.config(bg=b._h))
        b.bind("<Leave>", lambda e: b.config(bg=b._c))
        return b

    def _conv_tcvn3(self): self._run("tcvn3")
    def _conv_vni(self):   self._run("vni")

    def _run(self, mode):
        try:
            text = self.clipboard_get()
        except:
            text = ""
        if not text.strip():
            self._status("❌  Clipboard trống!", ERROR_C); return

        result = tcvn3_to_unicode(text) if mode == "tcvn3" else vni_to_unicode(text)
        self.clipboard_clear()
        self.clipboard_append(result)

        btn = self.btn_t if mode == "tcvn3" else self.btn_v
        btn.config(bg=SUCCESS, fg="#000")
        self.after(400, lambda: btn.config(bg=btn._c, fg=TEXT_W))
        label = "TCVN3→Unicode" if mode == "tcvn3" else "VNI→Unicode"
        self._status(f"✓  {label}  {len(result)} ký tự  —  Ctrl+V để dán", SUCCESS)

    def _status(self, msg, color=TEXT_DIM):
        self.sv.set(msg); self.sl.config(fg=color)
        if self._status_job: self.after_cancel(self._status_job)
        self._status_job = self.after(4000, lambda: (self.sv.set("Ctrl+C  →  bấm nút  →  Ctrl+V"), self.sl.config(fg=TEXT_DIM)))

if __name__ == "__main__":
    MiniConverter().mainloop()
