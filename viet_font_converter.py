# ===================================================================
# CONVERSION FUNCTIONS
# ===================================================================

def tcvn3_to_unicode(text: str) -> str:
    result = []
    for ch in text:
        code = ord(ch)
        if code in _TCVN3_TO_UNI:
            result.append(chr(_TCVN3_TO_UNI[code]))
        else:
            result.append(ch)
    return ''.join(result)


def vni_to_unicode(text: str) -> str:
    result = []
    i = 0
    n = len(text)
    while i < n:
        c1 = ord(text[i])
        matched = False
        if i + 1 < n:
            c2 = ord(text[i + 1])
            key = (c1, c2)
            if key in VNI_TWO_CHAR:
                result.append(chr(VNI_TWO_CHAR[key]))
                i += 2
                matched = True
        if not matched:
            if c1 in VNI_SINGLE:
                result.append(chr(VNI_SINGLE[c1]))
            else:
                result.append(text[i])
            i += 1
    return ''.join(result)

